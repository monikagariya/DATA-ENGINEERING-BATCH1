# Example 1: if statement
number = 10
if number > 0:
    print("Number is positive")

# Example 2: if-else statement
number = -5
if number > 0:
    print("Number is positive")
else:
    print("Number is non-positive")

# Example 3: while loop
counter = 0
while counter < 5:
    print("Counter:", counter)
    counter += 1

# Example 4: for loop
for i in range(3):
    print("Iteration:", i)

# Example 5: nested loops
for i in range(3):
    for j in range(2):
        print(f"Nested Iteration - i: {i}, j: {j}")







